/// Standard spacing scale: 8 / 16 / 24. Use consistently across app.
class AppSpacing {
  AppSpacing._();

  /// 4 — tight inline
  static const double xs = 4;

  /// 8 — compact
  static const double sm = 8;

  /// 16 — default (grid unit)
  static const double md = 16;
  static const double grid = 16;

  /// 24 — section / web content padding
  static const double lg = 24;

  /// 32 — large sections
  static const double xl = 32;

  /// 48 — extra large
  static const double xxl = 48;
}
